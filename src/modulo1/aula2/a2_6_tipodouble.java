package modulo1.aula2;

public class a2_6_tipodouble {
    public static void main(String args[]) {
        double menor_valor = Double.MIN_VALUE; // 1.4E-45
        double maior_valor = Double.MAX_VALUE; // 3.4028235E38
        System.out.println(menor_valor);
        System.out.println(maior_valor);
        double salario = 800.00;
        System.out.println(salario);
    }
}
